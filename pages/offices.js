import PropertiesContainer from '@/components/properties-container';
import Head from 'next/head';

export default function Departments({ data }) {
  return (
    <>
      <Head>
        <title>Oficinas | Homely</title>
        <meta name="description" content="Generated by create next app" />
      </Head>

      <PropertiesContainer data={data} title="Oficinas" />
    </>
  );
}

export async function getStaticProps() {
  const res = await fetch(`${process.env.API_URL}/api/properties?type=office`);
  const data = await res.json();

  return {
    props: {
      data,
    },
  };
}
